use std::fs;
use std::path::{Path, PathBuf};
use std::process::Command;

#[derive(Debug)]
pub enum WasmError {
    Io(std::io::Error),
    R1csError(String),
    BuildFailed(String),
    TemplateError(String),
    CommandFailed(String),
}

impl std::fmt::Display for WasmError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            WasmError::Io(e) => write!(f, "IO error: {}", e),
            WasmError::R1csError(msg) => write!(f, "R1CS parsing error: {}", msg),
            WasmError::BuildFailed(msg) => write!(f, "WASM build failed: {}", msg),
            WasmError::TemplateError(msg) => write!(f, "Template generation error: {}", msg),
            WasmError::CommandFailed(msg) => write!(f, "Command execution failed: {}", msg),
        }
    }
}

impl std::error::Error for WasmError {}

impl From<std::io::Error> for WasmError {
    fn from(error: std::io::Error) -> Self {
        WasmError::Io(error)
    }
}

pub struct WasmConfig {
    pub r1cs_path: PathBuf,
    pub output_dir: PathBuf,
    pub circuit_name: String,
}

pub fn generate_witness_calculator(config: WasmConfig) -> Result<(), WasmError> {
    let wasm_dir = config
        .output_dir
        .join(format!("{}_wasm", config.circuit_name.to_lowercase()));

    println!(
        "Generating WASM witness calculator for circuit: {}",
        config.circuit_name
    );
    println!("Output directory: {}", wasm_dir.display());

    create_wasm_crate_structure(&wasm_dir)?;

    let r1cs_data = fs::read(&config.r1cs_path)?;
    validate_r1cs_data(&r1cs_data)?;

    generate_cargo_toml(&wasm_dir, &config.circuit_name)?;
    generate_lib_rs(&wasm_dir, &config.circuit_name)?;
    embed_r1cs_data(&wasm_dir, &r1cs_data)?;
    generate_solver(&wasm_dir)?;
    generate_r1cs_types(&wasm_dir)?;

    build_wasm(&wasm_dir)?;

    generate_nodejs_wrapper(&wasm_dir, &config.circuit_name)?;

    println!("WASM witness calculator generated successfully!");
    println!("Generated files in: {}", wasm_dir.display());
    println!("Import from: {}/pkg/", wasm_dir.display());
    println!("Test with: node {}/generate_witness.js", wasm_dir.display());

    Ok(())
}

fn create_wasm_crate_structure(wasm_dir: &PathBuf) -> Result<(), WasmError> {
    fs::create_dir_all(wasm_dir)?;
    fs::create_dir_all(wasm_dir.join("src"))?;

    println!("Created WASM crate structure");
    Ok(())
}

fn validate_r1cs_data(r1cs_data: &[u8]) -> Result<(), WasmError> {
    if r1cs_data.len() < 8 {
        return Err(WasmError::R1csError("R1CS file too small".to_string()));
    }

    if &r1cs_data[0..8] != b"lof-r1cs" {
        return Err(WasmError::R1csError(
            "Invalid magic bytes - not a lof-r1cs file".to_string(),
        ));
    }

    println!("✅ R1CS file validation passed ({} bytes)", r1cs_data.len());
    Ok(())
}

