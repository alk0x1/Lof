fn generate_r1cs_types(wasm_dir: &Path) -> Result<(), WasmError> {
    let r1cs_types = r#"//! R1CS types and parsing logic
//! Compatible with Lof compiler R1CS format

use std::io::{self, Read};

/// R1CS constraint system containing circuit information
#[derive(Debug, Clone)]
pub struct ConstraintSystem {
    /// Names of public input signals
    pub public_inputs: Vec<String>,
    /// Names of witness (private) signals  
    pub witnesses: Vec<String>,
    /// R1CS constraints (A * B = C format)
    pub constraints: Vec<Constraint>,
}

/// Single R1CS constraint: A * B = C
#[derive(Debug, Clone)]
pub struct Constraint {
    pub a: LinearCombination,
    pub b: LinearCombination,
    pub c: LinearCombination,
}

/// Linear combination of variables with coefficients
#[derive(Debug, Clone)]
pub struct LinearCombination {
    /// Vector of (variable_index, coefficient) pairs
    pub terms: Vec<(u32, i64)>,
}

impl ConstraintSystem {
    /// Parse constraint system from byte array
    pub fn from_bytes(data: &[u8]) -> io::Result<Self> {
        let mut reader = std::io::Cursor::new(data);
        Self::from_reader(&mut reader)
    }
    
    /// Parse constraint system from reader
    pub fn from_reader(mut reader: impl Read) -> io::Result<Self> {
        // Read and verify magic bytes
        let mut magic = [0u8; 8];
        reader.read_exact(&mut magic)?;
        if &magic != b"lof-r1cs" {
            return Err(io::Error::new(
                io::ErrorKind::InvalidData,
                "Invalid magic bytes - not a lof-r1cs file"
            ));
        }

        // Read and verify version
        let mut version = [0u8; 4];
        reader.read_exact(&mut version)?;
        if u32::from_le_bytes(version) != 1 {
            return Err(io::Error::new(
                io::ErrorKind::InvalidData,
                "Unsupported r1cs version"
            ));
        }

        let mut buf = [0u8; 4];
        
        // Read counts
        reader.read_exact(&mut buf)?;
        let pub_inputs_count = u32::from_le_bytes(buf);
        
        reader.read_exact(&mut buf)?;
        let witnesses_count = u32::from_le_bytes(buf);
        
        reader.read_exact(&mut buf)?;
        let constraints_count = u32::from_le_bytes(buf);

        // Read public input names
        let mut public_inputs = Vec::new();
        for _ in 0..pub_inputs_count {
            reader.read_exact(&mut buf)?;
            let len = u32::from_le_bytes(buf) as usize;
            let mut name = vec![0u8; len];
            reader.read_exact(&mut name)?;
            public_inputs.push(String::from_utf8(name)
                .map_err(|e| io::Error::new(io::ErrorKind::InvalidData, e))?);
        }

        // Read witness names
        let mut witnesses = Vec::new();
        for _ in 0..witnesses_count {
            reader.read_exact(&mut buf)?;
            let len = u32::from_le_bytes(buf) as usize;
            let mut name = vec![0u8; len];
            reader.read_exact(&mut name)?;
            witnesses.push(String::from_utf8(name)
                .map_err(|e| io::Error::new(io::ErrorKind::InvalidData, e))?);
        }

        // Read constraints
        let mut constraints = Vec::new();
        for _ in 0..constraints_count {
            let a = read_linear_combination(&mut reader)?;
            let b = read_linear_combination(&mut reader)?;
            let c = read_linear_combination(&mut reader)?;
            constraints.push(Constraint { a, b, c });
        }

        Ok(Self {
            public_inputs,
            witnesses,
            constraints,
        })
    }
    
    /// Get total number of variables (public inputs + witnesses + ONE)
    pub fn get_variable_count(&self) -> usize {
        1 + self.public_inputs.len() + self.witnesses.len() // +1 for ONE
    }
}

/// Read a linear combination from binary format
fn read_linear_combination(reader: &mut impl Read) -> io::Result<LinearCombination> {
    let mut buf = [0u8; 4];
    reader.read_exact(&mut buf)?;
    let terms_count = u32::from_le_bytes(buf);
    
    let mut terms = Vec::new();
    for _ in 0..terms_count {
        // Read variable index
        reader.read_exact(&mut buf)?;
        let var_idx = u32::from_le_bytes(buf);
        
        // Read coefficient  
        let mut coeff_buf = [0u8; 8];
        reader.read_exact(&mut coeff_buf)?;
        let coeff = i64::from_le_bytes(coeff_buf);
        
        // Debug: Check if we're getting reasonable values
        if var_idx > 100 {  // Sanity check
            eprintln!("WARNING: Suspicious variable index: {}", var_idx);
        }
        
        terms.push((var_idx, coeff));
    }
    
    Ok(LinearCombination { terms })
}

impl std::fmt::Display for Constraint {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "(")?;
        for (i, (var, coeff)) in self.a.terms.iter().enumerate() {
            if i > 0 { write!(f, " + ")?; }
            write!(f, "{}*v{}", coeff, var)?;
        }
        write!(f, ") * (")?;
        for (i, (var, coeff)) in self.b.terms.iter().enumerate() {
            if i > 0 { write!(f, " + ")?; }
            write!(f, "{}*v{}", coeff, var)?;
        }
        write!(f, ") = (")?;
        for (i, (var, coeff)) in self.c.terms.iter().enumerate() {
            if i > 0 { write!(f, " + ")?; }
            write!(f, "{}*v{}", coeff, var)?;
        }
        write!(f, ")")
    }
}
"#;

    let types_path = wasm_dir.join("src").join("r1cs_types.rs");
    fs::write(types_path, r1cs_types)?;

    println!("Generated r1cs_types.rs");
    Ok(())
}

fn build_wasm(wasm_dir: &PathBuf) -> Result<(), WasmError> {
    println!("Building WASM package with wasm-pack...");

    // Clean previous build
    let pkg_dir = wasm_dir.join("pkg");
    if pkg_dir.exists() {
        fs::remove_dir_all(&pkg_dir)?;
    }

    // Make sure we're not in a workspace by checking for Cargo.lock and Cargo.toml in parent dirs
    let output = Command::new("wasm-pack")
        .args(["build", "--target", "web", "--out-dir", "pkg", "--release"])
        .current_dir(wasm_dir)
        .env("CARGO_TARGET_DIR", wasm_dir.join("target")) // Isolate build
        .output()
        .map_err(|e| WasmError::CommandFailed(format!("Failed to execute wasm-pack: {}", e)))?;

    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr);
        let stdout = String::from_utf8_lossy(&output.stdout);

        return Err(WasmError::BuildFailed(format!(
            "wasm-pack build failed:\nSTDOUT: {}\nSTDERR: {}",
            stdout, stderr
        )));
    }

    println!("✅ WASM build completed successfully");
    Ok(())
}

fn generate_nodejs_wrapper(wasm_dir: &Path, circuit_name: &str) -> Result<(), WasmError> {
    let wrapper_js = format!(
        r#"#!/usr/bin/env node
/**
 * Node.js witness calculator for {} circuit
 * Similar to Circom's generate_witness.js
 * 
 * Usage: node generate_witness.js <input.json> [output.json]
 */

const fs = require('fs');
const path = require('path');

// Import the WASM module
async function loadWasm() {{
    const wasmModule = await import('./pkg/{}_witness_calculator.js');
    await wasmModule.default(); // Initialize WASM
    return wasmModule;
}}

async function generateWitness(inputFile, outputFile) {{
    try {{
        // Load WASM module
        const wasm = await loadWasm();
        
        // Read input file
        if (!fs.existsSync(inputFile)) {{
            throw new Error(`Input file not found: ${{inputFile}}`);
        }}
        
        const inputData = fs.readFileSync(inputFile, 'utf8');
        console.log(`📖 Reading inputs from: ${{inputFile}}`);
        
        // Create witness calculator
        const calculator = new wasm.WitnessCalculator();
        
        // Print circuit info
        const info = JSON.parse(calculator.get_circuit_info());
        console.log(`🔧 Circuit: ${{info.circuit_name}}`);
        console.log(`📊 Constraints: ${{info.constraint_count}}`);
        console.log(`🔍 Public inputs: ${{info.public_input_count}}`);
        console.log(`🔒 Witnesses: ${{info.witness_count}}`);
        
        // Calculate witness
        console.log(`⚡ Calculating witness...`);
        const witnessJson = calculator.calculate_witness(inputData);
        const witness = JSON.parse(witnessJson);
        
        console.log(`✅ Witness calculated successfully!`);
        console.log(`📏 Witness size: ${{witness.length}} elements`);
        
        // Write output
        if (outputFile) {{
            fs.writeFileSync(outputFile, JSON.stringify(witness, null, 2));
            console.log(`💾 Witness saved to: ${{outputFile}}`);
        }} else {{
            console.log(`📄 Witness:`);
            console.log(JSON.stringify(witness, null, 2));
        }}
        
    }} catch (error) {{
        console.error(`❌ Error: ${{error.message}}`);
        process.exit(1);
    }}
}}

// Parse command line arguments
function main() {{
    const args = process.argv.slice(2);
    
    if (args.length === 0 || args.includes('--help') || args.includes('-h')) {{
        console.log(`
{} Circuit Witness Calculator

Usage:
    node generate_witness.js <input.json> [output.json]
    
Arguments:
    input.json    JSON file with circuit inputs
    output.json   Optional output file (prints to console if not specified)
    
Examples:
    node generate_witness.js input.json
    node generate_witness.js input.json witness.json
        `);
        return;
    }}
    
    const inputFile = args[0];
    const outputFile = args[1];
    
    generateWitness(inputFile, outputFile);
}}

// Run if called directly
if (require.main === module) {{
    main();
}}

module.exports = {{ generateWitness }};
"#,
        circuit_name,
        circuit_name.to_lowercase(),
        circuit_name
    );

    let wrapper_path = wasm_dir.join("generate_witness.js");
    fs::write(wrapper_path, wrapper_js)?;

    // Make it executable on Unix systems
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        let gen_witness_path = wasm_dir.join("generate_witness.js");
        let mut perms = fs::metadata(&gen_witness_path)?.permissions();
        perms.set_mode(0o755);
        fs::set_permissions(gen_witness_path, perms)?;
    }

    // Generate a simple browser example
    generate_browser_example(wasm_dir, circuit_name)?;

    println!("Generated Node.js wrapper and browser example");
    Ok(())
}

